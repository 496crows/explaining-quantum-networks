def _tidy(ax) -> None:
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.grid(True, alpha=0.8)
    ax.set_axisbelow(True)


def _family_of(graph_id: str, families: dict[str, str]) -> str:
    return families[graph_id]


def load_run() -> dict:
    corpus = json.loads((RUN / "corpus.json").read_text())
    oracle = json.loads((RUN / "oracle_summary.json").read_text())
    graphs = {g["graph_id"]: g for g in corpus["graphs"]}
    online = {}
    for path in sorted((RUN / "online_runs" / "exp3_vs_exp3").glob("*.json")):
        d = json.loads(path.read_text())
        online[d["graph_id"]] = d
    return {"graphs": graphs, "oracle": oracle, "online": online}


