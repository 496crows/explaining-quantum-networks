# Per-figure line colors (validated CVD-safe set: hunter green, blue,
# sunset orange, red); the figure's own legend carries family identity.
CONVERGENCE_COLOR = {
    "disjoint_parallel": "#2a78d6",   # blue
    "wheatstone_chain": "#c25e00",    # dark sunset orange
    "multi_bottleneck": "#e34948",    # red
    "layered_parallel": "#2e7d32",    # hunter green
}


def fig_convergence(data: dict) -> None:
    fig, ax_gap = plt.subplots(figsize=(COLUMN_IN, 2.1),
                               constrained_layout=True)

    stride = 20
    for graph_id in SELECTED:
        run = data["online"][graph_id]
        case = data["graphs"][graph_id]
        fam = case["family"]
        curve = run["learning_curve"][::stride]
        turns = np.array([row["turn"] for row in curve]) / 1e3
        gap = np.array([max(row["exploitability"], 1e-6) for row in curve])
        feats = case["features"]
        label = (f"{fam.replace('_', ' ')} "
                 f"(N={feats['node_disjoint_paths']:.0f}, "
                 f"h={feats['longest_hops']:.0f})")
        ax_gap.plot(turns, gap, color=CONVERGENCE_COLOR[fam], linewidth=1.2,
                    label=label)

    ax_gap.set_yscale("log")
    ax_gap.set_ylabel("Nash gap (averaged strategies)")
    ax_gap.set_xlabel(r"turn ($\times 10^3$)")
    ax_gap.legend(loc="upper right", handlelength=1.3, borderaxespad=0.2,
                  labelspacing=0.3)
    _tidy(ax_gap)
    fig.savefig(OUT / "fig_convergence.pdf")
    plt.close(fig)


