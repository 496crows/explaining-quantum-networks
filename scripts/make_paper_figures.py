def fig_retention_vs_paths(data: dict) -> None:
    fig, ax = plt.subplots(figsize=(COLUMN_IN, 2.55), constrained_layout=True)

    ideal_x = np.geomspace(1, 32, 128)
    ax.plot(ideal_x, 1 - 1 / ideal_x, color=INK, linestyle=(0, (2, 2)),
            linewidth=0.9, zorder=1)
    ax.annotate(r"$1-1/N$", xy=(11, 1 - 1 / 11), xytext=(9, 0.72),
                fontsize=7, color=INK_2)

    rng = np.random.default_rng(7)
    seen: list[str] = []
    for graph_id, oracle in data["oracle"].items():
        if oracle["retention"] is None:
            continue
        case = data["graphs"][graph_id]
        fam = case["family"]
        n = case["features"]["node_disjoint_paths"]
        jitter = np.exp(rng.uniform(-0.06, 0.06))  # multiplicative on log2 x
        ax.scatter(n * jitter, oracle["retention"], s=16,
                   marker=FAMILY_MARKER[fam],
                   facecolor=FAMILY_COLOR[fam], edgecolor="white",
                   linewidth=0.4, alpha=0.9, zorder=3,
                   label=fam.replace("_", " ") if fam not in seen else None)
        if fam not in seen:
            seen.append(fam)

    ax.annotate("bottleneck families:\nretention $\\approx 0$",
                xy=(1.05, 0.02), xytext=(1.15, 0.24), fontsize=6.5,
                color=INK_2,
                arrowprops=dict(arrowstyle="-", color=MUTED, linewidth=0.6))

    ax.set_xscale("log", base=2)
    ax.set_xticks([1, 2, 4, 8, 16, 32])
    ax.set_xticklabels(["1", "2", "4", "8", "16", "32"])
    ax.set_xlim(0.8, 40)
    ax.set_ylim(-0.05, 1.05)
    ax.set_xlabel("node-disjoint paths $N$")
    ax.set_ylabel("oracle retention")
    handles, labels = ax.get_legend_handles_labels()
    order = sorted(range(len(labels)), key=lambda i: labels[i])
    ax.legend([handles[i] for i in order], [labels[i] for i in order],
              loc="lower right", ncol=2, handletextpad=0.1,
              columnspacing=0.6, labelspacing=0.25, borderaxespad=0.2)
    _tidy(ax)
    fig.savefig(OUT / "fig_retention_vs_paths.pdf")
    plt.close(fig)


