def fig_attack_depth() -> None:
    import sys

    sys.path.insert(0, str(REPO))
    from sequence_game.experiments.exp3_sequence.attack_cache import (
        attack_cache_hop_statistics,
    )
    from sequence_game.experiments.exp3_sequence.baseline_cache import (
        baseline_cache_hop_statistics,
    )

    attack_rows = attack_cache_hop_statistics(
        REPO / "sequence_game" / "experiments" / "exp3_sequence"
        / "attack_baselines.sqlite")
    baseline_rows = baseline_cache_hop_statistics(
        REPO / "sequence_game" / "experiments" / "exp3_sequence"
        / "baselines_fidelity_0p98.sqlite")

    series: list[tuple[str, str, list[dict]]] = [
        ("no attack", INK_2, baseline_rows),
    ]
    by_kind: dict[str, list[dict]] = {}
    for row in attack_rows:
        by_kind.setdefault(str(row["attack_kind"]), []).append(row)
    kind_style = {
        "edge_intercept_resend": ("intercept–resend", "#e34948"),
        "memory_degradation": ("memory degradation", "#2a78d6"),
    }
    for kind in sorted(by_kind):
        label, color = kind_style.get(kind, (kind, INK))
        series.append((label, color, by_kind[kind]))

    fig, ax_s = plt.subplots(figsize=(COLUMN_IN, 2.2),
                             constrained_layout=True)

    for label, color, rows in series:
        rows = sorted(rows, key=lambda r: int(r["hop_count"]))
        hops = np.array([int(r["hop_count"]) for r in rows])
        mean = np.array([float(r["mean_abs_chsh_s"]) for r in rows])
        sd = np.array([float(r["sd_abs_chsh_s"]) for r in rows])
        ax_s.fill_between(hops, np.maximum(0, mean - sd), mean + sd,
                          color=color, alpha=0.12, linewidth=0)
        ax_s.plot(hops, mean, color=color, linewidth=1.5, label=label)

    ax_s.axhline(2.0, color=INK, linestyle=(0, (2, 2)), linewidth=0.8)
    ax_s.text(7.35, 2.06, "Bell bound $|S|=2$", ha="right", va="bottom",
              fontsize=6.5, color=INK_2)

    ax_s.set_ylabel(r"mean $|S|$  ($\pm$1 sd)")
    ax_s.set_ylim(0, 3.05)
    ax_s.set_xlabel("route length (hops)")
    ax_s.set_xticks(range(1, 8))
    fig.legend(loc="outside upper center", ncol=3, handlelength=1.3,
               columnspacing=1.0, handletextpad=0.5)
    _tidy(ax_s)
    fig.savefig(OUT / "fig_attack_depth.pdf")
    plt.close(fig)


