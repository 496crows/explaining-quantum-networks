def fig_strategy_heatmaps() -> None:
    action_rows = json.loads(
        (RUN / "dt" / "action_strategy_dt.json").read_text())["rows"]
    route_rows = json.loads(
        (RUN / "dt" / "route_strategy_dt.json").read_text())["rows"]

    kinds = ["no_attack", "edge_intercept_resend", "memory_degradation"]
    kind_labels = ["none", "edge IR", "memory"]
    eve_by_graph: dict[str, dict[str, float]] = {}
    fam_of: dict[str, str] = {}
    for row in action_rows:
        gid = row["graph_id"]
        fam_of[gid] = row["family"]
        kind = str(row["action_id"]).split(":", 1)[0]
        cell = eve_by_graph.setdefault(gid, dict.fromkeys(kinds, 0.0))
        cell[kind] += float(row["exp3_eve_empirical_prob"])
    eve = np.zeros((len(FAMILIES), len(kinds)))
    for i, fam in enumerate(FAMILIES):
        graphs = [g for g, f in fam_of.items() if f == fam]
        for j, kind in enumerate(kinds):
            eve[i, j] = float(np.mean([eve_by_graph[g][kind]
                                       for g in graphs]))

    max_rank = 8
    alice_by_graph: dict[str, list[float]] = {}
    for row in route_rows:
        alice_by_graph.setdefault(row["graph_id"], []).append(
            float(row["exp3_alice_empirical_prob"]))
    alice = np.zeros((len(FAMILIES), max_rank + 1))
    for i, fam in enumerate(FAMILIES):
        graphs = [g for g, f in fam_of.items() if f == fam]
        profiles = []
        for g in graphs:
            probs = sorted(alice_by_graph.get(g, []), reverse=True)
            prof = [probs[k] if k < len(probs) else 0.0
                    for k in range(max_rank)]
            prof.append(sum(probs[max_rank:]))
            profiles.append(prof)
        alice[i] = np.mean(np.asarray(profiles), axis=0)

    fig, (ax_e, ax_a) = plt.subplots(
        1, 2, figsize=(COLUMN_IN, 2.0), constrained_layout=True,
        width_ratios=[3, 9])
    cmap = plt.get_cmap("turbo")

    def draw(ax, data_matrix, col_labels):
        image = ax.imshow(data_matrix, cmap=cmap, vmin=0.0, vmax=1.0,
                          aspect="auto")
        ax.set_xticks(range(len(col_labels)))
        ax.set_xticklabels(col_labels, fontsize=6)
        ax.set_yticks(range(len(FAMILIES)))
        for i in range(data_matrix.shape[0]):
            for j in range(data_matrix.shape[1]):
                value = data_matrix[i, j]
                rgb = cmap(value)[:3]
                lum = 0.299 * rgb[0] + 0.587 * rgb[1] + 0.114 * rgb[2]
                ax.text(j, i, f"{value:.2f}".lstrip("0") or "0",
                        ha="center", va="center", fontsize=5,
                        color="black" if lum > 0.5 else "white")
        ax.tick_params(length=0)
        return image

    draw(ax_e, eve, kind_labels)
    ax_e.set_yticklabels([f.replace("_", " ") for f in FAMILIES],
                         fontsize=6.5)
    ax_e.set_title("(a) Eve: action type", fontsize=7, color=INK_2)
    ax_e.tick_params(axis="x", labelrotation=40)

    image = draw(ax_a, alice, [str(k) for k in range(1, max_rank + 1)]
                 + ["9+"])
    ax_a.set_yticklabels([])
    ax_a.set_title("(b) Alice: route rank", fontsize=7, color=INK_2)
    ax_a.set_xlabel("route rank (by learned probability)", fontsize=6.5)

    colorbar = fig.colorbar(image, ax=(ax_e, ax_a), fraction=0.035,
                            pad=0.015)
    colorbar.ax.tick_params(labelsize=6)
    colorbar.set_label("mean learned probability mass", fontsize=6.5)
    fig.savefig(OUT / "fig_strategy_heatmaps.pdf")
    plt.close(fig)


