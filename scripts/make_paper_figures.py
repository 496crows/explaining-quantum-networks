def fig_exploitability(data: dict) -> None:
    fig, ax_c = plt.subplots(figsize=(COLUMN_IN, 2.1),
                             constrained_layout=True)

    stride = 20
    curves = []
    for run in data["online"].values():
        curve = run["learning_curve"][::stride]
        turns = np.array([row["turn"] for row in curve]) / 1e3
        gap = np.array([max(row["exploitability"], 1e-6) for row in curve])
        curves.append(gap)
        ax_c.plot(turns, gap, color=AXIS, linewidth=0.5, alpha=0.45,
                  zorder=1)
    matrix = np.vstack(curves)
    ax_c.plot(turns, np.median(matrix, axis=0), color="#2a78d6",
              linewidth=1.6, zorder=3, label="median (50 graphs)")
    ax_c.fill_between(turns, np.percentile(matrix, 10, axis=0),
                      np.percentile(matrix, 90, axis=0), color="#2a78d6",
                      alpha=0.14, linewidth=0, zorder=2,
                      label="10–90%")
    ax_c.set_yscale("log")
    ax_c.set_xlabel(r"turn ($\times 10^3$)")
    ax_c.set_ylabel("Nash gap (averaged strategies)")
    ax_c.legend(loc="upper right", handlelength=1.4, borderaxespad=0.2)
    _tidy(ax_c)
    fig.savefig(OUT / "fig_exploitability.pdf")
    plt.close(fig)


